package managerXML;

public class Punto {

		private int x;
		private int y;
		
		public Punto(int posX,int posY) {
			
			x = posX;
			y = posY;
		}

		public int getX() {
			return x;
		}

		public void setX(int x) {
			this.x = x;
		}

		public int getY() {
			return y;
		}

		public void setY(int y) {
			this.y = y;
		}
}
